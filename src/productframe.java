    private void addbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addbtnActionPerformed
        String prodname = prname.getText();
        int prodqty = Integer.parseInt(prqty.getValue().toString());
        Float prodprice = Float.parseFloat(prprc.getValue().toString());
        int a = new productclass().checkprodname(prodname, prodqty, prodprice);
        //

        if(!"".equals(prodname)){
            if(a==0){
                int g = new productclass().addpro(prodname, prodqty, prodprice);
                if(g==1){
                    JOptionPane.showMessageDialog(addproduct, "Product is added", "SYSTEM MESSAGE", JOptionPane.INFORMATION_MESSAGE);
                    showtable();
                    clearfields();
                }
            }else{
                JOptionPane.showMessageDialog(addproduct, "Add to existing product", "SYSTEM MESSAGE", JOptionPane.INFORMATION_MESSAGE);
                showtable();
                clearfields();
            }
        }else{
            JOptionPane.showMessageDialog(addproduct, "Please make sure to fill all fields", "SYSTEM MESSAGE", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_addbtnActionPerformed

